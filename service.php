                <p id="top">
                At Evangel Presbyterian Church, we believe that the worship service is the time that we come together as a body and offer worship to our Triune God, through Jesus Christ. We believe that the audience for our worship service is God, not the person in the pew. We believe that the pastors and elders leading the service, as well as the musicians are there to lead the congregation in the worship of God. The elements of each worship service will have a theme dictated by the text and sermon for that Lord’s Day. Thus, the hymns, the call to Worship, the call to confession, the prayer of confession will all be directed by this theme. The goal of the Pastors and Elders at Evangel is that the worship of God each Lord’s Day be viewed as the Body coming before God and worshipping corporately as a body; not as a group of individuals who happen to be worshipping in the same place at the same time. Announcements and any other “housekeeping” items are typically covered before the commencement of the Worship Service to allow total attention to be focused on the corporate worship of our great God. What follows is an overview of the typical worship service at Evangel Presbyterian Church.
                </p>
                <br>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Greeting
                    </h2>
                    <h3 class="post-subtitle">
                        Grace and peace to you from God our Father, and the Lord Jesus Christ…
                        This greeting or some slight variation of it is used in the opening of every letter the Apostle Paul wrote in the New Testament. These letters were to be read publicly in the churches to which they were sent. Hence, we believe it is an appropriate opening to our Worship Service.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Call to Worship
                    </h2>
                    <h3 class="post-subtitle">
                        <span class="service">• Call to Worship: Psalm 29:1-2</span>
                        <span class="service">• Minister: Ascribe to the LORD, O mighty ones, ascribe to the LORD glory and strength.</span>
                        <span class="service" id="last_service">• People: Ascribe to the LORD the glory due His name; worship the LORD in the splendor of His holiness.</span>
                        Often , though not always, taken from Psalms, the call to worship is done antiphonally, or responsively. The Pastor begins the call with the congregation responding. The call always ends with the congregation as a whole giving response. The Pastor is not seen as calling the congregation to worship, rather this is viewed as the Pastor and congregation calling one another to worship.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Hymn of Adoration
                    </h2>
                    <h3 class="post-subtitle">
                        At least a portion of this hymn is in the second person (i.e. singing to God).
                        The opening lines of Psalm 8 would be an example of the Scriptural basis for this hymn.
                        O LORD, our Lord, how majestic is your name in all the earth!
                        You have set your glory above the heavens. (Psalm 8:1 ESV)
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Prayer of Adoration
                    </h2>
                    <h3 class="post-subtitle">
                        The Pastor then speaks for the congregation in addressing God with adoration and thanksgiving for who He is and for being in our midst as we worship. This Worship service is to God, it is for God and we express that in word and song.}}
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Gloria Patri
                    </h2>
                    <h3 class="post-subtitle">
                        Again the congregation joins together in singing to God.
                        Glory be to the Father and to the Son and to the Holy Ghost…
                        The very essence of Biblical worship for God’s people.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Ministry of Music
                    </h2>
                    <h3 class="post-subtitle">
                        Those participating in the ministry of music sing on behalf of the congregation and seek to lift the congregation in worship and focus attention on some specific aspect of God’s glory.
                        While this is often a solo or ensemble, neither the musicians nor the congregation view it as a performance. It is viewed as a ministry with God as the audience.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Call to Confession
                    </h2>
                    <h3 class="post-subtitle">
                        The portion of Scripture chosen for this event in the worship will remind us of our natural tendencies and our position before God. We are told in Scripture that we all sin and that it is an ongoing struggle even for God’s people. It will also be in keeping with the theme of the service.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Prayer of Confession
                    </h2>
                    <h3 class="post-subtitle">
                        The congregation joins in a unison prayer of confession. In this prayer God’s people come together as a body to confess their sin and seek God’s mercy in Christ; the only place where true mercy is to be found.
                        Throughout Scripture, we find the prophets and many others addressing God in confession. Daniel 9 is a good example. He is going to seek God’s favor, but he begins his prayer with confession of his and his people’s sin
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Lord’s Assurance of Pardon
                    </h2>
                    <h3 class="post-subtitle">
                        <span class="service">• Romans 8:1 (ESV)
                        <br>There is therefore now no condemnation for those who are in Christ Jesus.</span>
                        <span class="service" id="last_service">• 1John 1:9 (ESV)
                        <br>If we confess our sins, he is faithful and just to forgive us our sins and to cleanse us from all unrighteousness.</span>
                These two verses are good examples of the verses used in the assurance of pardon. This will always be a verse or verses of Scripture. Assurance of God's pardon can only come from God so it is taken from His Word.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Giving of Tithes and Offerings
                    </h2>
                    <h3 class="post-subtitle">
                        Biblically the ministers are sustained and the expenses of the work of God's house are provided for by the tithes and offerings of His people. This is viewed as an act of Worship and is therefore carried out as an integral part of the Worship Service.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        Doxology
                    </h2>
                    <h3 class="post-subtitle">
                        Having given the tithes and offerings. we return thanks in song to the source of all we have.
                        “Praise God from whom all blessings flow…”
                        We find the Apostle Paul bursting into doxology at the end of his theological teaching in the first 11 chapters of Romans.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        Hymn
                    </h2>
                    <h3 class="post-subtitle">
                        This Hymn will, in keeping with the theme of the service, be geared more toward preparing for the Scripture reading and sermon.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Pastoral Prayer
                    </h2>
                    <h3 class="post-subtitle">
                        Scripture commands that intercessory prayer be made and this is the time in the service that we do this as a body with this specific purpose. The Pastor voices intercessory prayer on behalf of the congregation.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Scripture Reading
                    </h2>
                    <h3 class="post-subtitle">
                        The Word of God is read to the Congregation and we give attention as God speaks to us through His Word.
                        In preparation for the preaching of God's Word the congregation asks God to, "Open our eyes, that we may see wonderful things from Thy Word."
                        Also, up to this point in the service, with the exception of the call to confession, we have been addressing God in prayer, praise and song. Through His Word read and preached we hear God speak to us.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Sermon
                    </h2>
                    <h3 class="post-subtitle">
                        God's Word preached is the primary means of the spread of the Gospel. The sermon will be the central piece of the Evangel Worship Service. We believe that nothing is a suitable substitute for the preaching of the Word.
                        Romans 10:14-17 (ESV)
                        How then will they call on him in whom they have not believed? And how are they to believe in him of whom they have never heard? And how are they to hear without someone preaching?
                        1Corinthians 1:21 (ESV)
                        For since, in the wisdom of God, the world did not know God through wisdom, it pleased God through the folly of what we preach to save those who believe.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Prayer of Application
                    </h2>
                    <h3 class="post-subtitle">
                        The Pastor then prays that God would apply the Word that has been read and preached to the lives of the congregation.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        Closing Hymn
                    </h2>
                    <h3 class="post-subtitle">
                        The congregation then raises its voice in a hymn that is often a hymn of application.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                        The Benediction
                    </h2>
                    <h3 class="post-subtitle">
                        God’s blessing was commanded to be pronounced over His people.
                        Numbers 6:24-25 (ESV)
                        The LORD bless you and keep you; 25the LORD make his face to shine upon you and be gracious to you; 26the LORD lift up his countenance upon you and give you peace.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                         The Lord's Supper
                    </h2>
                    <h3 class="post-subtitle">
                        Evangel Presbyterian Church celebrates the Sacrament of Communion, or the Lord's Supper the first Sunday of each month as a part of the Worship Service.
                    </h3>
                </div>
                <hr>
                <div class="home-item">
                    <h2 class="home-item-title">
                         Baptism
                    </h2>
                    <h3 class="post-subtitle">
                        As people come to faith in Christ and present themselves for baptism, or Communing members present their Covenant children for baptism, this sacrament is included in the worship service so that the congregation participates in the sacrament.
                    </h3>
